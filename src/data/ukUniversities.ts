// Common student email domains for validation
export const studentEmailDomains = [
  '.ac.uk',
  '.edu',
  '.sch.uk'
];

// Validate if email is a student email
export const isStudentEmail = (email: string): boolean => {
  const lowerEmail = email.toLowerCase();
  return studentEmailDomains.some(domain => lowerEmail.endsWith(domain));
};

